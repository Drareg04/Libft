int	ft_atoi(const char *nptr)
{
	int	sign;
	int	total;

	total = 0;
	sign = 1;
	while ((*nptr >= 9 && *nptr <= 13) || *nptr == ' ')
		nptr++;
	if (*nptr == '+' || *nptr == '-')
	{
		if (*nptr == '-')
			sign = -sign;
		nptr++;
	}
	while (*nptr >= '0' && *nptr <= '9')
	{
		total *= 10;
		total += *nptr - '0';
		nptr++;
	}
	total *= sign;
	return (total);
}
